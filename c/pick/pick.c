#define MAX_RECORD_SIZE 1440

#define CONST_PI 3.141592

static int org_find(float *pattern_T, int count, float slot_theta)
{
	int i;

	for(i = 0 ; i < count; i++) {
		if(pattern_T[i] > slot_theta) {
			return i;
		}
	}
	return count -1;
}

static int new_find(float *pattern_T, int count, float slot_theta)
{
	int i;
	int pick_idx;

	pick_idx = slot_theta / ((2 * CONST_PI) / count);


	if ( pick_idx >= count ) {
		pick_idx = count -1;
	}

	if ( pattern_T[pick_idx] > slot_theta ) {
		for ( i = pick_idx-1; i >= 0 ; i-- ) {
			if ( pattern_T[i] <= slot_theta ) {
				return i+1;
			}
		}
		return 0;
	} else {
		for ( i = pick_idx + 1; i < count ; i++ ) {
			if ( pattern_T[i] > slot_theta ) {
				return i;
			}
		}
		return count -1;
	}
}


