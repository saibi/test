static void unescape(unsigned char *dest, unsigned char *src, int len)
{
	int i, k;

	for ( i = 0, k = 0 ; i < len ; i ++, k++ ) {
		if ( src[i] == CHAR_ESC ) {
			i++;
			dest[k] = src[i] & (~0x80);
		} else {
			dest[k] = src[i];
		}
	}
}

